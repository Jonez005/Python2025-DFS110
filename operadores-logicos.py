#operadores logicos.

# and - as duas condições ser verdadeira para que a expressão seja verdadeira.
print(1 == 1 and 2 == 2)
                    #igual a 0 false

                
#OR - apenas i,a condição precisa ser verdadeira para que a expressão seja verdadeira.
print(1 > 2 or 10 < 14)

#Not - inverter o resultado da condição, verdadeiro vira falso e façso vira verdadeiro.
print(not 1 > 5)